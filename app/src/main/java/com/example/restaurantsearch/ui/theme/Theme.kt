@Composable
fun OnboardingScreen(
    onContinueClicked: () -> Unit,
    modifier: Modifier = Modifier
) {

    Column(
        modifier = modifier.fillMaxSize(),
        verticalArrangement = Arrangement.Center,
        horizontalAlignment = Alignment.CenterHorizontally
    ) {
        Text("検索範囲と金額を指定")

        InputDistance()
        InputMoney()

        Button(
            modifier = Modifier
                .padding(vertical = 24.dp),
            onClick = onContinueClicked
        ) {
            Text("お店を検索！")
        }
    }
}

@Composable
fun InputDistance(viewModel: SearchViewModel = viewModel())
{
    var distance by remember { mutableStateOf("") }
    OutlinedTextField(
        modifier = Modifier
            .padding(vertical = 6.dp),
        value = viewModel.distance,
        onValueChange = { distance -> viewModel.updateDistance(distance) },
        label = { Text("検索範囲を入力(m)") }
    )
}

@Composable
fun InputMoney(viewModel: SearchViewModel = viewModel()) {
    var money by remember { mutableStateOf("") }
    OutlinedTextField(
        modifier = Modifier
            .padding(vertical = 12.dp),
        value = viewModel.money,
        onValueChange = { money -> viewModel.updateMoney(money)},
        label = { Text("金額を入力(円)") }
    )
}

@Composable
fun SearchingScreen(
    navController: NavController,
    viewModel: SearchViewModel = viewModel()
    ) {
        val isCompleted by remember {
            derivedStateOf { viewModel.isCompleted }
        }
        LaunchedEffect(isCompleted) {
            if (isCompleted) {
                navController.navigate("next_screen") // 次の画面へ
            }
        }

        Column(
            modifier = Modifier.fillMaxSize(),
            verticalArrangement = Arrangement.Center,
            horizontalAlignment = Alignment.CenterHorizontally
        ) {
            Text("条件に合うレストランを検索中...")
            viewModel.SearchingRestaurant()
        }
}


@Composable
private fun Greetings(
    modifier: Modifier = Modifier,
    names: List<String> = List(1000) { "$it" }
) {
    LazyColumn(modifier = modifier.padding(vertical = 4.dp)) {
        items(items = names) { name ->
            Greeting(name = name)
        }
    }
}

@Composable
private fun Greeting(name: String, modifier: Modifier = Modifier) {
    Card(
        colors = CardDefaults.cardColors(
            containerColor = MaterialTheme.colorScheme.primary
        ),
        modifier = modifier.padding(vertical = 4.dp, horizontal = 8.dp)
    ) {
        CardContent(name)
    }
}

@Composable
private fun CardContent(name: String) {
    var expanded by rememberSaveable { mutableStateOf(false) }

    Row(
        modifier = Modifier
            .padding(12.dp)
            .animateContentSize(
                animationSpec = spring(
                    dampingRatio = Spring.DampingRatioMediumBouncy,
                    stiffness = Spring.StiffnessLow
                )
            )
    ) {
        Column(
            modifier = Modifier
                .weight(1f)
                .padding(12.dp)
        ) {
            Text(text = "Hello, ")
            Text(
                text = name, style = MaterialTheme.typography.headlineMedium.copy(
                    fontWeight = FontWeight.ExtraBold
                )
            )
            if (expanded) {
                Text(
                    text = ("Composem ipsum color sit lazy, " +
                            "padding theme elit, sed do bouncy. ").repeat(4),
                )
            }
        }
        IconButton(onClick = { expanded = !expanded }) {
            Icon(
                imageVector = if (expanded) Icons.AutoMirrored.Filled.ArrowBack else Icons.AutoMirrored.Filled.ArrowForward,
                contentDescription = if (expanded) {
                    stringResource(R.string.show_less)
                } else {
                    stringResource(R.string.show_more)
                }
            )
        }
    }
}


